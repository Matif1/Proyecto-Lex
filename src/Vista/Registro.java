    private void regustraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regustraActionPerformed
        String email;
        String us;
        String pass;
        email=mail.getText();
        us=ususario.getText();
        pass=contrasena.getText();
        
        String rol_abogado;
        rol_abogado=(String)ROL.getSelectedItem();
        AbogadoAdminDAO ad=new AbogadoAdminDAO();
        AbogadoDefensorDAO def=new AbogadoDefensorDAO();
        AbogadoEstudiosCausasDAO ac=new AbogadoEstudiosCausasDAO();
        if(rol_abogado=="Abogado administrador")
        {
          
            Abogado_administrador adim=new Abogado_administrador(email, us, pass);
            ad.ingresar_admin(adim);
            JOptionPane.showInternalInputDialog(this, "Felicidades, se ha registrado con  exito", "Registro de abogado",JOptionPane.INFORMATION_MESSAGE);
        }
        if(rol_abogado=="Abogado estudio de causas")
        {
          
            Abogado_estudio_causas adim=new Abogado_estudio_causas(email, us, pass);
            ac.registrar_abogado_estudio_causas(adim);
            JOptionPane.showInternalInputDialog(this, "Felicidades, se ha registrado con  exito", "Registro de abogado",JOptionPane.INFORMATION_MESSAGE);
        }
        if(rol_abogado=="Abogado defensor en tribunales")
        {
           
            Abogado_defensor_tribunales adim=new Abogado_defensor_tribunales(email, us, pass);
            def.registrar_abogado_defensaor(adim);
            JOptionPane.showInternalInputDialog(this, "Felicidades, se ha registrado con  exito", "Registro de abogado",JOptionPane.INFORMATION_MESSAGE);
        }
    
            

                
         
                

    
    
        
    }//GEN-LAST:event_regustraActionPerformed

